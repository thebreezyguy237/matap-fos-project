package models;

/**
 *
 * @author HP
 */
public class ShoppingCartItem {
    private int cartId;
    private Order order;
    private Food food;
    private int quantity;
    

    public ShoppingCartItem() {
    }

    public ShoppingCartItem(int cartId, Order order, Food food, int quantity) {
        this.cartId = cartId;
        this.order = order;
        this.food = food;
        this.quantity = quantity;
    }

    public int getCartId() {
        return cartId;
    }

    public void setCartId(int cartId) {
        this.cartId = cartId;
    }

    public Order getOrder() {
        return order;
    }

    public void setOrder(Order order) {
        this.order = order;
    }

    public Food getFood() {
        return food;
    }

    public void setFood(Food food) {
        this.food = food;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    
    
    
}
