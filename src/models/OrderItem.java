package models;

/**
 *
 * @author OLIVIA
 */
public class OrderItem {
    private int id;
    private Order order;
    private Food food;
    private int quantity;

    public OrderItem() {
    }

    public OrderItem(int id, Order order, Food food, int quantity) {
        this.id = id;
        this.order = order;
        this.food = food;
        this.quantity = quantity;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Order getOrder() {
        return order;
    }

    public void setOrder(Order order) {
        this.order = order;
    }

    public Food getFood() {
        return food;
    }

    public void setFood(Food food) {
        this.food = food;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    
    
}
